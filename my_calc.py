def add_numbers(num_1, num_2):
    result = num_1 + num_2

    return result

def sub_numbers(num_1, num_2):
    result = num_1 - num_2

    return result

def multiply_numbers(num_1, num_2):

    result = num_1 * num_2

    return result

def modulus_numbers(num_1, num_2):
    result = num_1 % num_2

    return result